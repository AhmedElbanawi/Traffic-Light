#define SEC_MAX_VAL 59
/******************************************************************************/
extern void SSD_Set_Symbol(const u8_t SYMBOL);
/******************************************************************************/
void TIME_Update(void){
     static u8_t Seconds = 0;
     if(Seconds < SEC_MAX_VAL){
        Seconds++;
     }else{
        Seconds = 0;
     }
     SSD_Set_Symbol(Seconds);
}
